/* c program to print helloWorld */

// Including the standard input / output header file 

// This header file provides functions like printf and scanf

#include<stdio.h>

// main function is the entry point of the program

// it returns an integer value to the operating system

// indicating the exit status of the program


// Note : using void  main() is not standard 

// The standard signature is int main(void) or int main(int argc, char *argv[])

int main(void){

	// printf is a standard library function used for output
	
	// It prints the specified string to the console

	printf("Hello World\n");

	// the return statement with 0 indicates successful execution

	return 0;	

}
